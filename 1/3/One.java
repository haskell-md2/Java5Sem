
public class One{ 
      
    public static void main (String args[]){
        
        /*Присваивание*/
        int a = 5;
        double b = 3.14;
        boolean flag = true;

        /*Арифметическое присваивание*/
        a += 2; // эквивалентно a = a + 2
        b -= 1.5; // эквивалентно b = b - 1.5
        flag &= false; // эквивалентно flag = flag && false

        /*Тернарный условный оператор*/
        double max = (a > b) ? a : b;

        /*Логические операторы*/
        if ((a > 0) || (b < 0)) {
            System.out.println("A больше нуля или B меньше нуля");
        }

        if (!(a == b)) { // аналогично if (a != b)
            System.out.println("A не равно B");
        }

        /*Побитовые операторы*/
        int x = 5; // двоичное представление: 0101
        int y = 3; // двоичное представление: 0011

        // Побитовое И (&):
        System.out.println(x & y); // выводит 1 (0001)

        // Побитовое ИЛИ (|):
        System.out.println(x | y); // выводит 7 (0111)

        // Исключающее ИЛИ (^):
        System.out.println(x ^ y); // выводит 6 (0110)

        // Побитовая инверсия (~):
        System.out.println(~x); // выводит -6 (двоичное дополнение)


        /*Битовые сдвиги*/
        int z = 8; // двоичное представление: 1000
        z <<= 1; // сдвиг влево на 1 бит, результат: 16 (10000)
        z >>= 1; // арифметический сдвиг вправо на 1 бит, результат: 8 (1000)
        z >>>= 1; // беззнаковый сдвиг вправо на 1 бит, результат: 4 (0100)

        /*Операции сравнения*/
        boolean result = (a == b);
        result = (a != b);
        result = (a < b);
        result = (a > b);
        result = (a <= b);
        result = (a >= b); 

        /*Инкремент и декремент*/
        int i = 10;
        i++; // post-increment - сначала используется значение, потом увеличивается
        ++i; // pre-increment - сначала увеличивается, потом используется новое значение

        int j = 10;
        j--; // post-decrement: сначала используется значение, потом уменьшается
        --j; // pre-decrement: сначала уменьшается, потом используется новое значение

        /*Конкатенация строк*/
        String s1 = "Hello";
        String s2 = "World";

        s1 += ", "; // добавляет запятую и пробел
        s1 += s2; // добавляет строку "World"

        System.out.println(s1); // выведет "Hello, World"
    }
}